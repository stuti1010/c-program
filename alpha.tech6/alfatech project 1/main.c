#include <stdio.h>
#include <stdlib.h>
 int main()

{
    printf("%d +%d =%d\n",5,2,5+2);
    printf("%d - %d =%d\n",5,2,5-2);
    printf("%d * %d =%d\n",5,2,5*2);
    printf("%d / %d = %.1d\n",5,2,5/2);// we take the full part of division
    printf("%d %% %d =%d\n",5,2,5%2);//taking remainder
    return 0;

}

