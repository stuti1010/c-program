#include <stdio.h>
#include <stdlib.h>

//print value and address of variables by using pointers//

int main()
{
    int grade1=80,grade2=95;
    printf("grade 1 is %d\n",grade1);
    printf("grade 1 address is %p\n",&grade1);
    printf(" grade 2 is %d\n",grade2);
    printf("grade 2 address is %p\n",grade2);

    return 0;


}
